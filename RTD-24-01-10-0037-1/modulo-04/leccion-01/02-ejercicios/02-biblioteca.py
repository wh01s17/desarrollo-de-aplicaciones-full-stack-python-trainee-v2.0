# Ejercicio 2 — Control de inventario de biblioteca
# Una biblioteca necesita llevar el control de sus libros disponibles para préstamo.
# Cada libro tiene un título, autor y cantidad de ejemplares.

# Enunciado
# Cree un sistema que permita:
# • Registrar varios libros.
# • Asociar cada libro a un identificador único.
# • Consultar el stock disponible.
# • Simular el préstamo de un libro disminuyendo su cantidad.

# Se espera que:
# • Los libros se almacenen en un diccionario.
# • Se acceda a un libro específico mediante su identificador.
# • Se utilicen ciclos para mostrar el inventario completo.
